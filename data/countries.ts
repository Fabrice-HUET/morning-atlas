import type { CountryBreakfast } from '@/types/country'

type CountrySeed = Pick<
  CountryBreakfast,
  | 'slug'
  | 'country'
  | 'countryCode'
  | 'flagEmoji'
  | 'region'
  | 'continent'
  | 'breakfastName'
  | 'shortDescription'
  | 'typicalItems'
  | 'commonDrinks'
  | 'breakfastStyles'
  | 'difficultyToRecreate'
  | 'ingredientSlugs'
  | 'categorySlugs'
  | 'tagSlugs'
> &
  Partial<
    Pick<
      CountryBreakfast,
      | 'heroTitle'
      | 'breakfastSubtitle'
      | 'culturalNote'
      | 'funFact'
      | 'morningContext'
      | 'recipeSlugs'
      | 'isTraditional'
      | 'needsReview'
    >
  >

function makeCountry(seed: CountrySeed): CountryBreakfast {
  return {
    heroTitle: `${seed.breakfastName} : un matin en ${seed.country}`,
    breakfastSubtitle: 'Une porte d entree culinaire, a nuancer selon les regions et les familles.',
    culturalNote: `Ce petit-dejeuner est souvent associe a ${seed.country}, sans resumer toutes les habitudes du pays.`,
    funFact: 'Les versions locales changent selon les saisons, les foyers et les produits disponibles.',
    morningContext: 'A explorer comme une piste culturelle, pas comme une definition unique du matin local.',
    recipeSlugs: [],
    isTraditional: true,
    seoTitle: `${seed.breakfastName} - petit-dejeuner en ${seed.country}`,
    seoDescription: seed.shortDescription,
    needsReview: true,
    ...seed,
  }
}

